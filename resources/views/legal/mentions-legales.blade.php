<h1>Politique de confidentialité</h1>

<p>Chez Discorev, nous attachons une grande importance à la protection de vos données personnelles. Ce document explique comment nous collectons, utilisons et sécurisons vos informations.</p>

<h2>Collecte des données</h2>
<p>Nous collectons uniquement les données nécessaires pour fournir nos services, notamment les informations fournies via nos formulaires ou votre compte utilisateur.</p>

<h2>Utilisation des données</h2>
<p>Les données collectées servent à la gestion de votre compte, à l'amélioration de nos services et à la communication nécessaire avec vous.</p>

<h2>Sécurité</h2>
<p>Nous mettons en place toutes les mesures techniques et organisationnelles pour protéger vos informations contre tout accès non autorisé.</p>

<p>Pour toute question concernant vos données personnelles, vous pouvez nous contacter à <a href="mailto:contact@discorev.com">contact@discorev.com</a>.</p>

