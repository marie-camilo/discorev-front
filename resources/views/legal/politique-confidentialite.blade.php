<br>
<h3>10. Évolution et innovation</h3>
<p>Discorev est une plateforme en constante évolution. En conséquence, cette politique couvre également :</p>
<ul>
    <li>les futurs modules, intégrations, fonctionnalités et outils de mesure de performance ;</li>
    <li>les technologies émergentes (intelligence artificielle, analyse sémantique, traitement automatique du langage, IA générative, analyse vidéo, etc.) ;</li>
    <li>tout nouveau mode de collecte ou de traitement conforme au RGPD.</li>
</ul>
<p>Discorev s’engage à maintenir un haut niveau de transparence et à informer ses utilisateurs en cas de changement substantiel des pratiques de traitement.</p>

