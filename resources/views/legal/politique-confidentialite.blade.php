<br>
<h3>1. Objet</h3>
<p>La présente Politique de confidentialité a pour objet d’informer l’ensemble des utilisateurs du site
    <a href="https://www.discorev.fr" target="_blank" rel="noopener">www.discorev.fr</a>
    et de ses sous-domaines (ci-après “le Site”) sur la manière dont Discorev SAS (ci-après “Discorev”, “Nous” ou “la Plateforme”) collecte, traite, conserve et protège leurs données personnelles, professionnelles et comportementales, actuelles ou futures.</p>
<p>Elle s’applique à tous les utilisateurs de Discorev : candidats, recruteurs, visiteurs, partenaires ou contributeurs ; et couvre l’ensemble des traitements réalisés dans le cadre des activités de la plateforme, présentes et à venir.</p>

<br>
<h3>2. Données collectées</h3>
<p>Discorev peut être amené à collecter, directement ou indirectement, tout type de données utiles au fonctionnement, à la personnalisation et à l’évolution de ses services, notamment :</p>

<h3>Données d’identification et de contact</h3>
<p>Nom, prénom, genre, photo, coordonnées postales, e-mail, téléphone, identifiants de connexion, profils publics (LinkedIn, etc.).</p>

<h3>Données professionnelles et institutionnelles</h3>
<p>CV, expériences, parcours, diplômes, compétences, statut professionnel, rôle, structure ou entreprise, identifiants administratifs, logo, description, effectif, type de contrat, préférences d’emploi ou de mission.</p>

<h3>Données d’activité et d’usage</h3>
<p>Recherches effectuées, pages consultées, temps de navigation, clics, formulaires soumis, interactions entre utilisateurs, messages envoyés, connexions et déconnexions, comportements sur le site et ses outils.</p>

<h3>Données multimédias et de contenu</h3>
<p>Images, vidéos, enregistrements, témoignages, présentations, interviews, contenus postés par les utilisateurs, ou transmis dans le cadre de la création d’une page entreprise ou d’une offre.</p>

<h3>Données techniques et analytiques</h3>
<p>Adresse IP, type d’appareil, navigateur, système d’exploitation, cookies, traceurs, géolocalisation approximative, statistiques d’usage, logs serveurs, données issues d’outils d’analyse ou d’apprentissage automatique.</p>

<h3>Données comportementales et dérivées</h3>
<p>Préférences, affinités, interactions avec les contenus, habitudes d’utilisation, analyses statistiques ou algorithmiques, données issues d’outils d’IA visant à améliorer la pertinence des mises en relation, recommandations et parcours utilisateurs.</p>

<h3>Données futures et innovantes</h3>
<p>Toute donnée nouvelle ou dérivée collectée dans le cadre de l’évolution du service (intégration d’outils IA, API externes, automatisations RH, analytics avancé, évaluation de compatibilité, tests vidéo ou vocaux), dès lors qu’elle reste conforme au RGPD et à la finalité du service.</p>

<br>
<h3>3. Finalités du traitement</h3>
<p>Les données sont collectées et utilisées par Discorev pour :</p>
<ul>
    <li>Fournir et gérer les services du Site (création de comptes, publication d’offres, recherche d’emploi, candidatures, communication) ;</li>
    <li>Mettre en relation les candidats, les recruteurs et les structures du secteur social, médico-social et sanitaire ;</li>
    <li>Personnaliser et améliorer l’expérience utilisateur (contenus, suggestions, navigation, matching) ;</li>
    <li>Valoriser la marque employeur des structures présentes sur le Site ;</li>
    <li>Produire des statistiques et analyses (comportement, performance, attractivité des métiers) ;</li>
    <li>Assurer la sécurité, la maintenance et la conformité technique et légale du Site ;</li>
    <li>Développer de nouveaux services, fonctionnalités ou outils d’aide au recrutement et à la mise en relation (y compris IA, automatisation, intégrations externes) ;</li>
    <li>Communiquer et promouvoir Discorev, ses utilisateurs et ses partenaires, dans le respect de leur image et de leurs droits.</li>
</ul>

<br>
<h3>4. Réutilisation des contenus publics</h3>
<p>Les éléments rendus publics par les utilisateurs (entreprises, candidats, médias, contenus, témoignages, visuels, offres, pages, vidéos, etc.) peuvent être réutilisés librement par Discorev à des fins de :</p>
<ul>
    <li>communication et promotion (site web, réseaux sociaux, newsletters, événements, supports commerciaux, médias partenaires) ;</li>
    <li>démonstration du service, études de cas, documentation interne ;</li>
    <li>analyse, recherche ou développement de produits ;</li>
    <li>valorisation du secteur, sensibilisation ou impact social.</li>
</ul>
<p>Discorev s’engage à respecter la dignité, l’image et la réputation des personnes et structures concernées, et à retirer tout contenu sur demande justifiée.</p>

<br>
<h3>5. Bases légales</h3>
<p>Les traitements de données effectués par Discorev reposent sur :</p>
<ul>
    <li>l’exécution d’un contrat (fourniture de services, gestion de comptes) ;</li>
    <li>le consentement explicite (profil public, cookies, communications marketing) ;</li>
    <li>l’intérêt légitime de Discorev (innovation, amélioration continue, sécurité, valorisation du secteur) ;</li>
    <li>le respect d’obligations légales (comptabilité, sécurité, conformité RGPD).</li>
</ul>

<br>
<h3>6. Destinataires des données</h3>
<p>Les données peuvent être partagées avec :</p>
<ul>
    <li>les recruteurs et candidats concernés lors de mises en relation ;</li>
    <li>les prestataires techniques et partenaires contractuels assurant l’hébergement, la maintenance, la sécurité, l’analyse de données ou la communication ;</li>
    <li>les partenaires institutionnels contribuant à la valorisation du secteur (dans le respect de la confidentialité) ;</li>
    <li>les autorités administratives ou judiciaires si la loi l’exige.</li>
</ul>
<p>Discorev ne revend pas de données personnelles à des tiers.</p>

<br>
<h3>7. Durée de conservation</h3>
<p>Les données sont conservées pour la durée nécessaire à la réalisation des finalités décrites ci-dessus, puis :</p>
<ul>
    <li>supprimées ou anonymisées lorsque leur conservation n’est plus justifiée ;</li>
    <li>ou archivées dans le respect des obligations légales (notamment comptables et contractuelles).</li>
</ul>
<p>Discorev peut conserver certaines données sous forme agrégée, anonymisée ou statistique afin d’améliorer ses services, d’étudier le marché et d’évaluer son impact social.</p>

<br>
<h3>8. Sécurité et confidentialité</h3>
<p>Discorev met en œuvre toutes les mesures techniques et organisationnelles nécessaires pour garantir la confidentialité, l’intégrité et la protection des données :</p>
<ul>
    <li>chiffrement, sauvegardes, contrôle d’accès, pare-feux ;</li>
    <li>audits de sécurité, procédures internes de gestion des accès ;</li>
    <li>politiques de gestion des incidents et de suppression des comptes.</li>
</ul>
<p>En cas de violation de données, les utilisateurs et la CNIL seront informés conformément à la réglementation en vigueur.</p>

<br>
<h3>9. Vos droits</h3>
<p>Conformément au RGPD et à la loi “Informatique et Libertés”, chaque utilisateur dispose des droits suivants :</p>
<ul>
    <li>Accès à ses données personnelles ;</li>
    <li>Rectification ou suppression de ses informations ;</li>
    <li>Limitation ou opposition à certains traitements ;</li>
    <li>Portabilité des données (transfert à un autre fournisseur) ;</li>
    <li>Retrait du consentement à tout moment ;</li>
    <li>Droit à l’effacement (“droit à l’oubli”).</li>
</ul>
