<br>
<h3>2. Qu’est-ce qu’un cookie ?</h3>
<p>Un cookie est un petit fichier texte déposé sur votre appareil (ordinateur, smartphone, tablette) lors de la consultation d’un site Internet. Il permet de reconnaître votre appareil, de collecter certaines informations de navigation, et de faciliter ou personnaliser votre expérience.</p>
<p>Il existe plusieurs types de cookies et technologies similaires :</p>
<ul>
    <li>Cookies HTTP (stockés dans votre navigateur) ;</li>
    <li>Cookies de session (temporaires) et cookies persistants (longue durée) ;</li>
    <li>Traceurs publicitaires ou analytiques ;</li>
    <li>Pixels invisibles / balises web ;</li>
    <li>Stockage local et technologies équivalentes (fingerprinting, SDKs).</li>
</ul>

<br>
<h3>3. Les cookies que nous utilisons</h3>
<p>Discorev utilise plusieurs catégories de cookies afin d’assurer le bon fonctionnement du site et d’améliorer les services proposés.</p>

<h5>Cookies strictement nécessaires</h5>
<p><strong>Finalité :</strong> assurer le bon fonctionnement du Site et des services essentiels (connexion, sécurité, authentification, sauvegarde des préférences, gestion de session).</p>
<p><strong>Exemple :</strong> cookies de session, jetons de connexion, gestion des formulaires.</p>
<p><strong>Base légale :</strong> intérêt légitime de Discorev (exemption de consentement selon la CNIL).</p>

<h5>Cookies de mesure d’audience et d’analyse</h5>
<p><strong>Finalité :</strong> mesurer la fréquentation, l’utilisation du Site, détecter les anomalies et améliorer les performances.</p>
<p><strong>Exemple :</strong> Google Analytics (ou tout autre outil équivalent), outils internes de reporting.</p>
<p><strong>Données traitées :</strong> pages consultées, durée de session, clics, sources de trafic, interactions.</p>
<p><strong>Base légale :</strong> consentement (collecté via la bannière cookies).</p>

<h5>Cookies de personnalisation et d’expérience utilisateur</h5>
<p><strong>Finalité :</strong> personnaliser le contenu, adapter les offres ou suggestions aux préférences des utilisateurs.</p>
<p><strong>Exemple :</strong> suggestions d’entreprises, offres d’emploi similaires, préférences de recherche.</p>
<p><strong>Base légale :</strong> consentement de l’utilisateur.</p>

<h5>Cookies de communication et marketing</h5>
<p><strong>Finalité :</strong> mesurer l’efficacité de nos campagnes, suivre les inscriptions et interagir avec nos partenaires (réseaux sociaux, emailing, médias externes).</p>
<p><strong>Exemple :</strong> traceurs Meta (Facebook/Instagram), LinkedIn Insight Tag, Google Ads, newsletters.</p>
<p><strong>Base légale :</strong> consentement explicite.</p>

<h5>Cookies technologiques et futurs traceurs</h5>
<p><strong>Finalité :</strong> permettre le développement de nouvelles fonctionnalités (IA, analyse sémantique, recommandation de profils, matching intelligent).</p>
<p><strong>Données :</strong> interactions, préférences, historiques de navigation, signaux comportementaux.</p>
<p><strong>Base légale :</strong> consentement, recueilli au moment de l’introduction de ces outils.</p>

